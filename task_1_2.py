x = 5
y = 6
res = x & y
print(f'Результат побитового 5 И 6 = {res}')
res = x | y
print(f'Результат побитового 5 ИЛИ 6 = {res}')
res = x ^ y
print(f'Результат побитового исключающего 5 ИЛИ 6 = {res}')
res = ~x
print(f'Результат побитового НЕ 5 = {res}')
res = x >> 2
print(f'Результат побитового сдвига 5 вправо на 2 знака = {res}')
res = x << 2
print(f'Результат побитового сдвига 5 влево на 2 знака = {res}')
